#include<iostream>
using namespace std;

int main(){

    float a,b,sum,diff,prod,division;

    cin>>a;
    cout<<"Enter the value of a = "<<a<<endl;
    
    cin>>b;
    cout<<"Enter the value of b = "<<b<<endl;
    

    sum=a+b;
    cout<<"Sum = "<<sum<<endl;

    diff=a-b;
    cout<<"Diff = "<<diff<<endl;

    prod=a*b;
    cout<<"Prod = "<<prod<<endl;

    division=b/a;
    cout<<"Division = "<<division<<endl;

    return 0;
    
}